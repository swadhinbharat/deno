/**
 * Generate a greetings for the addresses as provided.
 * @param name Name of addressee
 * @returns Greetings
 */
export function hello(name: string): string {
  return "Hello " + name;
}

/**
 * Generate a Hello World!!!
 * @returns Greetings
 */
 export function helloworld(): string {
  return "Hello World!!!";
}

